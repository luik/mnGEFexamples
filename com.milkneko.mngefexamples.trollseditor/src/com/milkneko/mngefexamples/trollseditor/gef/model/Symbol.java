package com.milkneko.mngefexamples.trollseditor.gef.model;

public class Symbol {
	private char characterID = '0';
	private int index = -1;

	public char getCharacterID() {
		return characterID;
	}

	public void setCharacterID(char characterID) {
		this.characterID = characterID;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}
	
}
